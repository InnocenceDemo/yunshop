package com.sample;


public class Goodsmsg {

  private long id;
  private String gsname;
  private String address;
  private String gdes;
  private double price;
  private String gimg;
  private String threeItemize;
  private long gcode;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getGsname() {
    return gsname;
  }

  public void setGsname(String gsname) {
    this.gsname = gsname;
  }


  public String getAddress() {
    return address;
  }

  public void setAddress(String address) {
    this.address = address;
  }


  public String getGdes() {
    return gdes;
  }

  public void setGdes(String gdes) {
    this.gdes = gdes;
  }


  public double getPrice() {
    return price;
  }

  public void setPrice(double price) {
    this.price = price;
  }


  public String getGimg() {
    return gimg;
  }

  public void setGimg(String gimg) {
    this.gimg = gimg;
  }


  public String getThreeItemize() {
    return threeItemize;
  }

  public void setThreeItemize(String threeItemize) {
    this.threeItemize = threeItemize;
  }


  public long getGcode() {
    return gcode;
  }

  public void setGcode(long gcode) {
    this.gcode = gcode;
  }

}
